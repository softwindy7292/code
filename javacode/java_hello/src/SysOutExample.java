/*
주석의 목적 : 소스코드 설명

*/
public class SysOutExample {

	public static void main(String[] args) {
		System.out.println(7); // 정수
		System.out.println(3.15); // 실수
		System.out.println(3 + 5);
		System.out.println("3 + 5");
		System.out.println("3 + 5 = " + 8);
		System.out.println("3 + 5 = " + (8 + 5.5));
		System.out.println("3 + 5 = " + 8 + 5.5);
	}

}